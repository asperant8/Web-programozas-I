<div class="cardbox"><p><strong>Mission</strong></p>

<p>Lost Dog & Cat Rescue Foundation helps homeless pets find their way to loving homes through rescue and adoption.</p>

<p><strong>Vision</strong></p>

<p>A world free of homeless, neglected or abused pets.</p>

<p><strong>History</strong></p>

<p>The Lost Dog & Cat Rescue Foundation (LDCRF) was incorporated as a 501c3 not-for-profit organization in 2001 when Pam McAlwee and Ross Underwood, a couple of Arlington, VA restaurant owners, decided to formalize their efforts to save homeless pets in their community. They purchased 63 acres of land in Sumerduck VA and began building The Lost Dog Ranch, a kennel facility for homeless pets with plenty of outdoor space for dogs to run and a cage-free building for cats.</p>

<p>Since 2001, LDCRF has saved the lives of more than 40,000 homeless pets, most of whom were facing the threat of euthanasia at over-crowded municipal shelters.</p>

<p>Lost Dog & Cat Rescue Foundation is overwhelmingly a volunteer-run organization with a small paid staff providing direct care to animals at the Lost Dog & Cat ‘Rescue Care Center’ facility located in Falls Church, Virginia. Many other animals are cared for in our network of volunteer foster homes and some cats are housed at Petsmart Cat Adoption Centers.</p>

<p>Every weekend, LDCRF holds off-site adoption events at local PetSmart and PetCo stores and places more than 2,000 dogs and cats per year for adoption. </p>

<p><strong>Values</strong></p>

<p>LDCRF was founded with the belief that through an open, friendly adoption process we could match great people with homeless pets and send those pets to their new homes without unnecessary barriers. To that end, we have done same day adoptions with two-week trial periods since 2001, and continue to look for ways to remove barriers that prevent adoptions.</p>

<p>We also believe that a rescued pet is a spayed or neutered pet, with no exceptions other than life-threatening medical conditions. We are very proud that every pet adopted through LDCRF since 2001 has been spayed or neutered prior to being placed in the hands of the adoptive family.</p>

<p>LDCRF makes a lifetime commitment to the animals we rescue and will always accept those animals back into our care should the need arise.</p>


<p>At Lost Dog we like pets AND people and we hope to make the adoption process positive and fun for adopters and volunteers alike.</p></div>